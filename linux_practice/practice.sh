#!/usr/bin/env bash
# =============================================================================
#  Практика по модулю Linux
#  Курс "Аналитик данных" — simulative.ru
# -----------------------------------------------------------------------------
#  Скрипт последовательно выполняет 13 задач практики.
#  Рекомендуется запускать через:   bash practice.sh
#  Аргумент --info вызывает демонстрационную функцию.
# =============================================================================

# --- Предисловие: завершать скрипт при первой ошибке -------------------------
set -e

# ============================================================================
#  Задача 1. Переменная путей
# ============================================================================
#  Создаём переменную с путём к файлу и выводим её на экран.
#  basename возвращает только имя файла без пути.
export MYFILE="$PWD/MYFILE.txt"
echo "==> [1] Путь к файлу: $MYFILE"
echo "    Имя файла (basename): $(basename "$MYFILE")"

# ============================================================================
#  Задача 2. Вывод операционной системы
# ============================================================================
echo "==> [2] Полная информация об ОС (/etc/os-release):"
cat /etc/os-release
echo "    Строки, содержащие 'UBUNTU' (без учёта регистра):"
cat /etc/os-release | grep -i "ubuntu" || echo "    (совпадений по 'ubuntu' не найдено)"

# ============================================================================
#  Задача 3. Обновление системы (только для root)
# ============================================================================
#  EUID — числовой ID пользователя; 0 означает root.
if [ "$EUID" -eq 0 ]; then
    echo "==> [3] Скрипт запущен от root — обновляем систему..."
    apt update && apt upgrade -y
else
    echo "==> [3] Скрипт запущен не от root — обновление системы пропущено."
fi

# ============================================================================
#  Задача 4. Временный alias внутри скрипта
# ============================================================================
#  shopt -s expand_aliases разрешает использование алиасов в скрипте.
shopt -s expand_aliases
alias qq='echo HELLO'
echo "==> [4] Вызов alias qq:"
qq

# ============================================================================
#  Задача 5. Создание файла лога с перезаписыванием
# ============================================================================
#  Если лог уже существует — удаляем, затем создаём пустой файл.
if [ -f logfile.log ]; then
    rm logfile.log
fi
touch logfile.log
echo "==> [5] Создан пустой файл лога: logfile.log"

# ============================================================================
#  Задача 6. Вывод списка файлов текущей директории в лог (дописывание)
# ============================================================================
#  >> дописывает в файл, не перезаписывая его.
ls >> logfile.log
echo "==> [6] Список файлов добавлен в logfile.log"

# ============================================================================
#  Задача 7. Подсчёт строк в файле лога
# ============================================================================
echo "==> [7] Количество строк в logfile.log:"
wc -l logfile.log

# ============================================================================
#  Задача 8. Использование переменных окружения
# ============================================================================
echo "==> [8] Ранее созданная переменная MYFILE: $MYFILE"
echo "    Системная переменная HOME: $HOME"
echo "    Системная переменная PATH: $PATH"

# ============================================================================
#  Задача 9. Функции
# ============================================================================
my_function() {
    echo "Это моя функция"
}
echo "==> [9] Вызов функции my_function:"
my_function

# ============================================================================
#  Задача 10. Вызов скрипта с аргументами
# ============================================================================
#  $1 — первый аргумент командной строки.
if [ "$1" == "--info" ]; then
    echo "==> [10] Получен аргумент --info — вызываем функцию:"
    my_function
else
    echo "==> [10] Аргумент --info не передан (получено: '${1:-<пусто>}')."
fi

# ============================================================================
#  Задача 11. Права на файл
# ============================================================================
#  777 — полные права для владельца, группы и остальных.
chmod 777 logfile.log
echo "==> [11] Установлены права 777 на logfile.log"

# ============================================================================
#  Задача 12. ТОП-3 процессов по памяти -> в лог
# ============================================================================
#  ps aux выводит все процессы, --sort=-%mem сортирует по памяти по убыванию,
#  head -4 берёт шапку + 3 процесса (итого 4 строки).
ps aux --sort=-%mem | head -4 >> logfile.log
echo "==> [12] ТОП-3 процессов по памяти добавлены в logfile.log"

# ============================================================================
#  Задача 13. Простейший CURL
# ============================================================================
echo "==> [13] Запрос к API шуток:"
curl -s https://official-joke-api.appspot.com/random_joke
echo ""

echo "========================================="
echo "  Практика по модулю Linux завершена. ✓"
echo "========================================="
